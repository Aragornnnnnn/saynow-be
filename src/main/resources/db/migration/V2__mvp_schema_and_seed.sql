CREATE TABLE scenario_categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_key VARCHAR(50) NOT NULL,
    name VARCHAR(50) NOT NULL,
    description VARCHAR(255),
    sort_order INT NOT NULL,
    status VARCHAR(20) NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT uk_scenario_categories_category_key UNIQUE (category_key)
);

CREATE TABLE scenarios (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_id BIGINT NOT NULL,
    scenario_key VARCHAR(80) NOT NULL,
    title VARCHAR(100) NOT NULL,
    situation_description TEXT NOT NULL,
    success_goal VARCHAR(255) NOT NULL,
    opening_babsae_text VARCHAR(500) NOT NULL,
    opening_tts_url VARCHAR(500),
    max_follow_up_count INT NOT NULL,
    thumbnail_url VARCHAR(500),
    sort_order INT NOT NULL,
    status VARCHAR(20) NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_scenarios_category FOREIGN KEY (category_id) REFERENCES scenario_categories (id),
    CONSTRAINT uk_scenarios_scenario_key UNIQUE (scenario_key)
);

CREATE INDEX idx_scenarios_category_order ON scenarios (category_id, sort_order);

CREATE TABLE scenario_slots (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    scenario_id BIGINT NOT NULL,
    slot_key VARCHAR(80) NOT NULL,
    display_name VARCHAR(100) NOT NULL,
    slot_order INT NOT NULL,
    required BOOLEAN NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_scenario_slots_scenario FOREIGN KEY (scenario_id) REFERENCES scenarios (id),
    CONSTRAINT uk_scenario_slots_scenario_slot UNIQUE (scenario_id, slot_key)
);

CREATE INDEX idx_scenario_slots_scenario_order ON scenario_slots (scenario_id, slot_order);

CREATE TABLE practice_sessions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id CHAR(36) NOT NULL,
    user_id BIGINT,
    scenario_id BIGINT NOT NULL,
    status VARCHAR(20) NOT NULL,
    max_follow_up_count INT NOT NULL,
    started_at TIMESTAMP(6) NOT NULL,
    ended_at TIMESTAMP(6),
    exit_reason VARCHAR(100),
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    version BIGINT NOT NULL,
    CONSTRAINT fk_practice_sessions_scenario FOREIGN KEY (scenario_id) REFERENCES scenarios (id),
    CONSTRAINT uk_practice_sessions_public_id UNIQUE (public_id)
);

CREATE INDEX idx_practice_sessions_user_created ON practice_sessions (user_id, created_at);
CREATE INDEX idx_practice_sessions_status_created ON practice_sessions (status, created_at);

CREATE TABLE session_metrics (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id BIGINT NOT NULL,
    metric_key VARCHAR(80) NOT NULL,
    metric_value BIGINT NOT NULL,
    unit VARCHAR(20) NOT NULL,
    recorded_at TIMESTAMP(6) NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_session_metrics_session FOREIGN KEY (session_id) REFERENCES practice_sessions (id),
    CONSTRAINT uk_session_metrics_session_key UNIQUE (session_id, metric_key)
);

CREATE TABLE session_prompts (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id BIGINT NOT NULL,
    prompt_index INT NOT NULL,
    prompt_type VARCHAR(30) NOT NULL,
    prompt_text VARCHAR(500) NOT NULL,
    tts_url VARCHAR(500),
    created_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_session_prompts_session FOREIGN KEY (session_id) REFERENCES practice_sessions (id),
    CONSTRAINT uk_session_prompts_session_index UNIQUE (session_id, prompt_index)
);

CREATE INDEX idx_session_prompts_session_created ON session_prompts (session_id, created_at);

CREATE TABLE practice_turns (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id BIGINT NOT NULL,
    prompt_id BIGINT NOT NULL,
    turn_index INT NOT NULL,
    user_transcript TEXT NOT NULL,
    input_type VARCHAR(20) NOT NULL,
    speech_started_after_ms INT,
    recording_duration_ms INT,
    stt_confidence NUMERIC(5,4),
    audio_object_key VARCHAR(500),
    created_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_practice_turns_session FOREIGN KEY (session_id) REFERENCES practice_sessions (id),
    CONSTRAINT fk_practice_turns_prompt FOREIGN KEY (prompt_id) REFERENCES session_prompts (id),
    CONSTRAINT uk_practice_turns_session_turn UNIQUE (session_id, turn_index),
    CONSTRAINT uk_practice_turns_prompt UNIQUE (prompt_id)
);

CREATE INDEX idx_practice_turns_session_created ON practice_turns (session_id, created_at);

CREATE TABLE session_slot_values (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id BIGINT NOT NULL,
    scenario_slot_id BIGINT NOT NULL,
    source_turn_id BIGINT NOT NULL,
    slot_value VARCHAR(255) NOT NULL,
    filled_at TIMESTAMP(6) NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_session_slot_values_session FOREIGN KEY (session_id) REFERENCES practice_sessions (id),
    CONSTRAINT fk_session_slot_values_slot FOREIGN KEY (scenario_slot_id) REFERENCES scenario_slots (id),
    CONSTRAINT fk_session_slot_values_turn FOREIGN KEY (source_turn_id) REFERENCES practice_turns (id),
    CONSTRAINT uk_session_slot_values_session_slot UNIQUE (session_id, scenario_slot_id)
);

CREATE INDEX idx_session_slot_values_session ON session_slot_values (session_id);
CREATE INDEX idx_session_slot_values_turn ON session_slot_values (source_turn_id);

CREATE TABLE session_feedbacks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id BIGINT NOT NULL,
    scenario_result VARCHAR(20) NOT NULL,
    total_understood_score INT NOT NULL,
    summary TEXT NOT NULL,
    average_score_delta INT NOT NULL,
    feedback_status VARCHAR(20) NOT NULL,
    generated_at TIMESTAMP(6),
    created_at TIMESTAMP(6) NOT NULL,
    updated_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_session_feedbacks_session FOREIGN KEY (session_id) REFERENCES practice_sessions (id),
    CONSTRAINT uk_session_feedbacks_session_id UNIQUE (session_id)
);

CREATE TABLE turn_feedbacks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_feedback_id BIGINT NOT NULL,
    turn_id BIGINT NOT NULL,
    understood_score INT NOT NULL,
    heard_as TEXT NOT NULL,
    better_expression VARCHAR(500),
    score_delta INT NOT NULL,
    improved_understood_score INT NOT NULL,
    reason TEXT NOT NULL,
    created_at TIMESTAMP(6) NOT NULL,
    CONSTRAINT fk_turn_feedbacks_session_feedback FOREIGN KEY (session_feedback_id) REFERENCES session_feedbacks (id),
    CONSTRAINT fk_turn_feedbacks_turn FOREIGN KEY (turn_id) REFERENCES practice_turns (id),
    CONSTRAINT uk_turn_feedbacks_feedback_turn UNIQUE (session_feedback_id, turn_id)
);

